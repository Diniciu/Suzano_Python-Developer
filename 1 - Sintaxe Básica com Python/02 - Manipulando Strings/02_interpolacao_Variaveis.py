# Método de interpolação com format (f) 

nome = "Vinícius"
idade = 23
profissao = "Estudante"
linguagem = "Python"
salario_min_atual = 1512
salario_min_real = 2500

print(f"Olá, meu nome é {nome}, tenho {idade} anos e sou {profissao}. Estou aprendendo {linguagem}.")
print(f"O salário mínimo atual é de R${salario_min_atual:.2f}, mas deveria ser de R${salario_min_real:.2f}.")